#Numbering Datatype

a = 5
b = 5.5
c = 5 + 4j

print("A:",type(a))
print("B:",type(b))
print("C:",type(c))

#Sequecing Datatype

stringobj = "Hello World"
listobj = [1,2,3,4]
tuple = (1,2,3,4)

print("String:",type(stringobj))
print("List:",type(listobj))
print("Tuple:",type(tuple))


#Mapping Datatype

dict = {"name":"ABC","city":"pune"}
setobj = {1,2,3,4}

